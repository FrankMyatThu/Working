DECLARE @Value VARCHAR(MAX)
DECLARE @i INT
SET @Value = 'asiatechnic@gmail.com'
SET @i = 1
WHILE (@i <= LEN(@Value))
BEGIN
	INSERT INTO tbl_Array (Id, ColumnA) VALUES (@i, SUBSTRING(@Value, @i, 1));
